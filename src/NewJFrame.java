    private void simpanPesanan(String email, String film, String jam, String kursi) {
        try (java.io.FileWriter writer = new java.io.FileWriter("pesanan.txt", true)) {
            writer.write(email + "," + film + "," + jam + "," + kursi + System.lineSeparator());
        } catch (java.io.IOException e) {
            e.printStackTrace();
        }
    }
    
    private java.util.Set<String> getKursiTerisi(String film, String jam) {
        java.util.Set<String> terisi = new java.util.HashSet<>();
        try (java.util.Scanner scanner = new java.util.Scanner(new java.io.File("pesanan.txt"))) {
            while (scanner.hasNextLine()) {
                String[] data = scanner.nextLine().split(",");
                if (data.length == 4 && data[1].equals(film) && data[2].equals(jam)) {
                    terisi.add(data[3]);
                }
            }
        } catch (java.io.FileNotFoundException e) {
            // File belum ada, artinya belum ada kursi terisi
        }
        return terisi;
    }

