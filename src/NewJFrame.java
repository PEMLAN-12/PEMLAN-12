    private void pilihanJamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pilihanJamActionPerformed
        pilihanKursi.removeAllItems();

        String selectedFilm = (String) pilihanFilm.getSelectedItem();
        String selectedJam = (String) pilihanJam.getSelectedItem();

        if (selectedFilm == null || selectedJam == null) return;

        String[] semuaKursi = {"A1", "A2", "A3", "A4", "A5", "B1", "B2", "B3", "B4", "B5", "C1", "C2", "C3", "C4", "C5"};
        java.util.Set<String> kursiTerisi = getKursiTerisi(selectedFilm, selectedJam);

        for (String k : semuaKursi) {
            if (!kursiTerisi.contains(k)) {
                pilihanKursi.addItem(k);
            }
        }

        jLabel5.setVisible(true);
        pilihanKursi.setVisible(true);
    }//GEN-LAST:event_pilihanJamActionPerformed

    private void btnPesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesanActionPerformed
        btnPesan.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                String email = jTextField1.getText();
                String film = (String) pilihanFilm.getSelectedItem();
                String jam = (String) pilihanJam.getSelectedItem();
                String kursi = (String) pilihanKursi.getSelectedItem();

                if (email.isEmpty() || film == null || jam == "Pilihan Jam" || kursi == "Pilihan Kursi") {
                    javax.swing.JOptionPane.showMessageDialog(null, "Lengkapi semua data terlebih dahulu!");
                    return;
                }

                // Cek apakah kursi sudah terisi (validasi ganda)
                if (getKursiTerisi(film, jam).contains(kursi)) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Kursi sudah dipesan! Silakan pilih kursi lain.");
                    return;
                }

                simpanPesanan(email, film, jam, kursi);
                javax.swing.JOptionPane.showMessageDialog(null, "Tiket berhasil dipesan!\nFilm: " + email + "\nEmail: " + film + "\nJam: " + jam + "\nKursi: " + kursi);
            }
        });
    }//GEN-LAST:event_btnPesanActionPerformed

